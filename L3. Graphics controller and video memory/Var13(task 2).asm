        USE16
        ORG     100h
;-----------------------------------------------------------------------------
;_______________________________
        MOV     AX,      04F02h
        MOV     BX,      00012h
        INT     010h
;_______________________________
        PUSH    0A000h
        POP     ES
;-----------------------------------------------------------------------------

        CALL    RECTANGLE

;_______________________________
        MOV     AX,      00000h
        INT     016h
;________________________________
        MOV     AX,      04C00h
        INT     021h

;================ ПРОЦЕДУРА ПОБУДОВИ ЗОБРАЖЕННЯ ПРЯМОКУТНИКА =================
RECTANGLE:
;______________________________
        MOV     DX,      003CEh
        MOV     AL,      000h
        OUT     DX,      AL
        INC     DX
        MOV     AL,      00Fh
        OUT     DX,      AL
;______________________________
        MOV     DX,      003CEh
        MOV     AL,      001h
        OUT     DX,      AL
        INC     DX
        MOV     AL,      00Bh
        OUT     DX,      AL
;_______________________________
        MOV     DI,      12830
        MOV     AL,      000h
        MOV     CX,      160
RECT1:  XCHG    BX,      CX
        MOV     CX,      24
        REP STOSB
        ADD     DI,      056
        XCHG    BX,      CX
        LOOP    RECT1

        RET
